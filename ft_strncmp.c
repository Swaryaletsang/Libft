#include "libft.h"

int			ft_strncmp(const char *s1, const char *s2, size_t n)
{
	unsigned int	i;

	i = 0;
	while (i < n)
	{
		return (ft_strcmp(s1, s2));
		i++;
	}
	return (0);
}
