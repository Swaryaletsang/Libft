#include "libft.h"

char		*ft_strnew(size_t size)
{
	char	*str;
	size_t	i;

	str = (char *)malloc(size);
	i = 0;
	while (i != size)
	{
		str[i] = '\0';
		i++;
	}
	if (!(str))
		return (NULL);
	return (str);
}
